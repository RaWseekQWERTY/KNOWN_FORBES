    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // Update the existing arraylist and table by cheching if it exists or not through rank
        try {
            String inputRank = tfUpdateRank.getText().trim();
            //check if the hiven rank input is valid 
            if (!inputRank.isEmpty()) {

                int existingRank = Integer.parseInt(inputRank);
                boolean updateForbes = true;
                KnownForbes existingForbes = null;
                int rowIndex = -1;
                boolean listExist = false;
                // Search for the KnownForbes object with the existing rank in the ArrayList
                for (int i = 0; i < listForbes.size(); i++) {
                    if (listForbes.get(i).getRank() == existingRank) {
                        existingForbes = listForbes.get(i);
                        rowIndex = i;
                        listExist = true;
                        break;
                    }
                }
                //if existing data of KnownForbes is found with given rank then proceed to update
                if (listExist) {
                    int updatedRank = Integer.parseInt(inputRank);
                    String updatedFullName = tfUpdateFirstName.getText().trim() + " " + tfUpdateLastName.getText().trim();
                    double updatedNetworth = Double.parseDouble(tfUpdateNetworth.getText().trim());
                    int updatedAge = (int) spinnerUpdateAge.getValue();
                    String updatedGender = "";
                    String updatedCountry = (String) cbUpdateCountry.getSelectedItem();
                    String updatedSource = tfUpdateSource.getText().trim();
                    String updatedScore = tfUpdateScore.getText().trim();
                    int updatedPhilScore = Integer.parseInt(updatedScore);

                    if (messages.checkNumber(updatedSource) || messages.checkNumber(updatedFullName)) {
                        updateForbes = false;
                        JOptionPane.showMessageDialog(null, "Source or name contains a number");

                    }

                    if (rbUpdateMale.isSelected()) {
                        updatedGender = "Male";
                    } else if (rbUpdateFemale.isSelected()) {
                        updatedGender = "Female";
                    } else {
                        JOptionPane.showMessageDialog(null, "Please select a gender");
                        updateForbes = false;
                    }
                    if (cbUpdateCountry.getSelectedIndex() == 0) {
                        JOptionPane.showMessageDialog(null, "Please select a Country");
                        updateForbes = false;
                    }
                    if (updatedPhilScore > 5) {
                        updateForbes = false;
                        JOptionPane.showMessageDialog(null, "Philanthrophy Score cannot be greater than 5");

                    }
                    if (updateForbes) {
                        //Update the KnownForbes object
                        existingForbes.updateForbes(updatedRank, updatedFullName, updatedNetworth, updatedAge,
                                updatedGender, updatedCountry, updatedSource, updatedPhilScore);
                        listForbes.set(rowIndex, existingForbes);
                        // Update the table with new value
                        updateTable();
                        JOptionPane.showMessageDialog(null, "Updated Successfully");
                    }
                } else {
                    JOptionPane.showMessageDialog(null, "No entry found with the given Rank.");
                }

            }
        } catch (NumberFormatException ex) {
            JOptionPane.showMessageDialog(null, "Invalid!!!Please check all input values again");
        } catch (NullPointerException nil) {
            JOptionPane.showMessageDialog(null, "Please Fill all fields");
        }

    }//GEN-LAST:event_btnUpdateActionPerformed


