    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        // Search Button ActionEvent listener to search through items in combobox of Category
        try {
            BinarySearch searchAlgorithm = new BinarySearch();
            String target = tfSearch.getText().trim();
            //to search through rank
            if (cbCategory.getSelectedIndex() == 0) {
                int targetRank = Integer.parseInt(target);
                SelectionSort sortAlgorithm = new SelectionSort();
                sortAlgorithm.selectionSortByRankAsc(listForbes);
                KnownForbes result = searchAlgorithm.binarySearchByRank(targetRank, listForbes);
                messages.displayResult(result);
                //to search through networth
            } else if (cbCategory.getSelectedIndex() == 1) {

                double targetNetworth = Double.parseDouble(target);
                InsertionSort sortAlgorithm = new InsertionSort();
                sortAlgorithm.insertionSortByNetworthAsc(listForbes);
                List<KnownForbes> results = searchAlgorithm.binarySearchByNetworth(targetNetworth, listForbes);
                for (KnownForbes outputResult : results) {
                    messages.displayResult(outputResult);
                }

            } //to search through fullName
            else if (cbCategory.getSelectedIndex() == 2) {
                if (messages.checkNumber(target)) {
                    JOptionPane.showMessageDialog(this, "search through strings only ", "Invalid input", JOptionPane.ERROR_MESSAGE);
                } else {
                    MergeSort sortAlgorithm = new MergeSort();
                    sortAlgorithm.mergeSortByFullNameAsc(listForbes);
                    String targetName = target.trim();
                    String[] name = targetName.split(" ");
                    if (name.length > 1) {
                        KnownForbes result = searchAlgorithm.binarySearchByFullName(target.trim(), listForbes);
                        messages.displayResult(result);
                    } else {
                        KnownForbes result = searchAlgorithm.binarySearchByName(targetName, listForbes);
                        messages.displayResult(result);
                    }
                }
                //to search through source
            } else if (cbCategory.getSelectedIndex() == 3) {
                if (messages.checkNumber(target)) {
                    JOptionPane.showMessageDialog(this, "search through strings only ", "Invalid input", JOptionPane.ERROR_MESSAGE);
                } else {
                    BubbleSort sortAlgorithm = new BubbleSort();
                    sortAlgorithm.bubbleSortBySourceAsc(listForbes);
                    List<KnownForbes> result = searchAlgorithm.binarySearchBySource(listForbes, target);
                    for (KnownForbes outputResult : result) {
                        messages.displayResult(outputResult);
                    }

                }

            }
        } catch (NumberFormatException numError) {
            JOptionPane.showMessageDialog(this, "Please check search values properly ", "Invalid input", JOptionPane.ERROR_MESSAGE);

        } catch (NullPointerException nil) {
            JOptionPane.showMessageDialog(null, "Billionaire not found.", "Not Found", JOptionPane.WARNING_MESSAGE);

        }
    }//GEN-LAST:event_btnSearchActionPerformed

