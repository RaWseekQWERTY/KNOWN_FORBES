    private void pnlDeleteDashMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnlDeleteDashMouseClicked
        // Delete existing data by clicking a row and remove from arraylist and hashmao as well
        row = tblBillionaires.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Please select at least one row !!", "Not selected row !!", JOptionPane.ERROR_MESSAGE);
        } else {
            try {
                // Remove the row from the JTable
                tmForbes.removeRow(row);

                // Remove the KnownForbes object from the listForbes ArrayList
                KnownForbes removedBillionaire = listForbes.remove(row);

                // Remove the rank from rankMap
                rankMap.remove(removedBillionaire.getRank());

                // Update the table and size
                tmForbes.setRowCount(0);
                for (int i = 0; i < listForbes.size(); i++) {
                    Object[] value = {listForbes.get(i).getRank(), listForbes.get(i).getFullName(), listForbes.get(i).getNetworth(),
                        listForbes.get(i).getAge(), listForbes.get(i).getGender(), listForbes.get(i).getCountry(),
                        listForbes.get(i).getSource(), listForbes.get(i).getPhilanthropyScore()};
                    tmForbes.addRow(value);
                }
                tableSize--;

            } catch (IndexOutOfBoundsException nullIndex) {
                JOptionPane.showMessageDialog(null, "Select a valid row", "Invalid row selection", JOptionPane.ERROR_MESSAGE);

            }
        }
    }//GEN-LAST:event_pnlDeleteDashMouseClicked

