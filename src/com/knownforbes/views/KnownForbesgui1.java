    private void btnDescSortActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDescSortActionPerformed
        // Descending sort button action perform by selecting through category

        //Descending sort through Rank by using Selection sort algorithm
        if (cbCategory.getSelectedIndex() == 0) {
            SelectionSort sortAlgorithm = new SelectionSort();
            sortAlgorithm.selectionSortByRankDesc(listForbes);
            updateTable();
            //Descending sort through Networth by using insertion sort algorithm
        } else if (cbCategory.getSelectedIndex() == 1) {
            InsertionSort sortAlgorithm = new InsertionSort();
            sortAlgorithm.insertionSortByNetworthDesc(listForbes);
            updateTable();

        } //Descending sort through Name by using merge sort algorithm
        else if (cbCategory.getSelectedIndex() == 2) {
            MergeSort sortAlgorithm = new MergeSort();
            sortAlgorithm.mergeSortByFullNameDesc(listForbes);
            updateTable();

        } //Descending sort through Source
        else if (cbCategory.getSelectedIndex() == 3) {
            BubbleSort sortAlgorithm = new BubbleSort();
            sortAlgorithm.bubbleSortBySourceDesc(listForbes);
            updateTable();
        }
    }//GEN-LAST:event_btnDescSortActionPerformed

