    private void pnlOKMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_pnlOKMouseClicked
        // Ok icon click to load data if adjacent input rank mapches with existing rank in KnownForbes Object
        String getRankinput = tfUpdateRank.getText().trim();
        if (getRankinput != null && !getRankinput.isEmpty()) {
            try {
                int rankExist = Integer.parseInt(getRankinput);
                KnownForbes forbes = null;
                //check if the rank is in arrayList or not
                for (KnownForbes forbesObj : listForbes) {
                    if (forbesObj.getRank() == rankExist) {
                        forbes = forbesObj;
                        break;
                    }
                }
                //if rank is in arrayList then set textfields with existing datas
                if (forbes != null) {
                    String[] fullName = forbes.getFullName().split(" ");
                    if (fullName.length >= 1) {
                        tfUpdateFirstName.setText(fullName[0]);
                    }
                    if (fullName.length >= 2) {
                        tfUpdateLastName.setText(fullName[1]);
                    }
                    tfUpdateNetworth.setText(String.valueOf(forbes.getNetworth()));
                    spinnerUpdateAge.setValue(forbes.getAge());
                    cbUpdateCountry.setSelectedItem(forbes.getCountry());
                    tfUpdateSource.setText(forbes.getSource());
                    tfUpdateScore.setText(String.valueOf(forbes.getPhilanthropyScore()));
                    JOptionPane.showMessageDialog(null, "Data loaded.");

                } else {
                    JOptionPane.showMessageDialog(null, "No entry found with the given Rank.");
                }
            } catch (NumberFormatException numError) {
                JOptionPane.showMessageDialog(null, "Enter valid input", "!!!Invalid!!!",
                        JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_pnlOKMouseClicked

