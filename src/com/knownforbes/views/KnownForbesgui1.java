    private void getInitalTableData() {
        // row count
        int rowCount = tmForbes.getRowCount();
        //for - row count
        // Model class data insert (getValueAt(i, 0), )
        // listOfForbes.add(model);
        //for end
        for (int i = 0; i < rowCount; i++) {
            int rank = (int) Integer.parseInt(tblBillionaires.getValueAt(i, 0).toString());
            String fullName = tblBillionaires.getValueAt(i, 1).toString();
            String networthString = tblBillionaires.getValueAt(i, 2).toString();
            String[] arrNetworth = networthString.split(" ");
            String networthNum = arrNetworth[0];
            Double networth = Double.valueOf(networthNum);
            int age = (int) Integer.parseInt(tblBillionaires.getValueAt(i, 3).toString());
            String gender = tblBillionaires.getValueAt(i, 4).toString();
            String country = tblBillionaires.getValueAt(i, 5).toString();
            String source = tblBillionaires.getValueAt(i, 6).toString();
            int philScore = (int) Integer.parseInt(tblBillionaires.getValueAt(i, 7).toString());

            KnownForbes objForbes = new KnownForbes(rank, fullName, networth, age, gender, country, source, philScore);
            listForbes.add(objForbes);
        }
    }

    // Method to update your table with data from listForbes
    private void updateTable() {
        tmForbes.setRowCount(0); // Clear existing data in the table
        for (KnownForbes billionaire : listForbes) {
            Object[] rowData = {
                billionaire.getRank(),
                billionaire.getFullName(),
                billionaire.getNetworth() + " " + "B",
                billionaire.getAge(),
                billionaire.getGender(),
                billionaire.getCountry(),
                billionaire.getSource(),
                billionaire.getPhilanthropyScore()
            };
            tmForbes.addRow(rowData);
        }
    }

